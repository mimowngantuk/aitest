function getColorTheory(color){

if(color=="black"){
return {
title:"Black",
desc:"Black represents elegance, power, and mystery."
};
}

if(color=="white"){
return {
title:"White",
desc:"White gives clean, minimal, and calm vibes."
};
}

if(color=="blue"){
return {
title:"Blue",
desc:"Blue feels calm, trustworthy, and relaxed."
};
}

if(color=="yellow"){
return {
title:"Yellow",
desc:"Yellow feels energetic, cheerful, and bold."
};
}

if(color=="pink"){
return {
title:"Pink",
desc:"Pink gives soft, warm, and playful vibes."
};
}

return {
title:"Unknown",
desc:"Neutral style."
};

}
